public class ConversaOidade {
    public static void main(String[] args) {
        int idade = 18; // idade em anos

        int meses = idade * 12;
        int dias = idade * 365;

        System.out.println("Idade: " + idade + " anos");
        System.out.println("Equivale a " + meses + " meses");
        System.out.println("Ou " + dias + " dias");
    }
}
